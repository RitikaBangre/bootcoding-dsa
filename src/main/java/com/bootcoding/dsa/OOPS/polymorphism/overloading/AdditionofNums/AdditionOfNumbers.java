package com.bootcoding.dsa.OOPS.polymorphism.overloading.AdditionofNums;

public class AdditionOfNumbers {
    public int add(int a, int b) {
        return a + b;
    }

    public double add(double a, double b) {
        return a + b;
    }
    public int add(int a ,int b, int c){
        return a+b+c;
    }
}

